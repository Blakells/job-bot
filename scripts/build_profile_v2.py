# ── Migration Helper ─────────────────────────────────────────────────────────

def migrate_old_profile(old_profile_path, user_name):
    """Migrate the old profiles/job_profile.json into profiles/{name}/."""
    old_path = Path(old_profile_path)
    if not old_path.exists():
        return False

    new_dir = PROFILES_DIR / user_name
    new_dir.mkdir(parents=True, exist_ok=True)
    new_path = new_dir / "profile.json"

    if new_path.exists():
        print("  ⚠️  Profile already exists at {}".format(new_path))
        return False

    profile = json.loads(old_path.read_text())
    new_path.write_text(json.dumps(profile, indent=2))
    print("  ✅ Migrated {} → {}".format(old_path, new_path))
    return True


# ── Main ─────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(
        description="Build a job search profile from your documents",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  # Interactive mode (recommended)
  python3 scripts/build_profile_v2.py

  # Skip the menu — go straight to a specific person
  python3 scripts/build_profile_v2.py --name hannah

  # Rebuild from scratch
  python3 scripts/build_profile_v2.py --name hannah --rebuild

  # Migrate old profile to new folder structure
  python3 scripts/build_profile_v2.py --migrate alex
        """,
    )
    parser.add_argument("--name", required=False,
                        help="Person's name (skips startup menu)")
    parser.add_argument("--rebuild", action="store_true",
                        help="Rebuild profile from scratch even if one exists")
    parser.add_argument("--migrate", metavar="NAME",
                        help="Migrate old profiles/job_profile.json to profiles/{NAME}/")

    args = parser.parse_args()

    print("\n🚀 Job Bot — Profile Builder v2")
    print("=" * 55)

    # ── Handle migration ─────────────────────────────────────────────

    if args.migrate:
        migrate_old_profile("profiles/job_profile.json", args.migrate)
        print("\n  ➡️  Now run: python3 scripts/build_profile_v2.py --name {}".format(args.migrate))
        return

    # ── Determine user via menu or --name flag ───────────────────────

    if args.name:
        # Direct mode — skip menu
        dir_name = args.name.lower().replace(" ", "_")
        user_dir = PROFILES_DIR / dir_name
        user_dir.mkdir(parents=True, exist_ok=True)
        has_existing = (user_dir / "profile.json").exists()
    else:
        # Interactive startup menu
        result = run_startup_menu()
        if result is None:
            return
        user_dir, dir_name, has_existing = result

    profile_path = user_dir / "profile.json"

    # ── Check for existing profile ───────────────────────────────────

    if has_existing and profile_path.exists() and not args.rebuild:
        print("\n  📋 Found existing profile for '{}'".format(dir_name))
        existing = json.loads(profile_path.read_text())
        display_profile_summary(existing)

        if not ask_user_yes_no("Rebuild this profile from scratch?", "n"):
            print("\n  ✅ Keeping existing profile. Use --rebuild to start over.")
            print("  ➡️  Next: python3 scripts/score_jobs.py --profile {}".format(profile_path))
            return

    # ── Document check ───────────────────────────────────────────────

    doc_result = run_document_check(user_dir)

    if doc_result is None:
        print("\n  👋 No problem! Drop your files into {} and run again.".format(user_dir))
        return

    classified, other_files = doc_result

    # ── Read all documents ───────────────────────────────────────────

    print("\n  📖 Reading documents...")
    documents = {}

    for doc_type, path in classified.items():
        content = read_file(path)
        if content:
            documents[doc_type] = content
            print("    ✅ {}: {} chars".format(DOC_LABELS.get(doc_type, doc_type), len(content)))
        else:
            print("    ⚠️  {}: could not read".format(DOC_LABELS.get(doc_type, doc_type)))

    for path in other_files:
        content = read_file(path)
        if content:
            key = "additional_{}".format(path.stem)
            documents[key] = content
            print("    ✅ {}: {} chars".format(path.name, len(content)))

    if not documents:
        print("\n  ❌ Could not read any documents. Check file formats.")
        return

    # ── Extract with Claude ──────────────────────────────────────────

    extracted = extract_profile_from_docs(documents)

    if not extracted:
        print("  ❌ Could not extract profile data. Check your API key and try again.")
        return

    p = extracted.get("personal", {})
    print("\n  ✅ Extraction complete!")
    print("  " + "─" * 50)
    print("  Name detected:  {}".format(p.get("name", "Unknown")))
    print("  Roles detected: {}".format(", ".join(extracted.get("extracted_roles", [])[:4])))
    print("  Skills found:   {}".format(len(extracted.get("hard_skills", []))))
    print("  Experience:     {} years".format(extracted.get("years_of_experience", "?")))

    # ── Run interactive Q&A ──────────────────────────────────────────

    profile = run_interactive_qa(extracted)

    # ── Save profile ─────────────────────────────────────────────────

    profile_path.write_text(json.dumps(profile, indent=2))
    display_profile_summary(profile)

    print("\n  📁 Saved to: {}".format(profile_path))
    print("\n  ➡️  Next steps:")
    print("     1. Review: cat {}".format(profile_path))
    print("     2. Find jobs: python3 scripts/score_jobs.py --profile {}".format(profile_path))
    print("     3. Or re-run: python3 scripts/build_profile_v2.py --name {} --rebuild".format(dir_name))


if __name__ == "__main__":
    main()
